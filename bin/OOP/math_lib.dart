import 'dart:math';

main() {
  print('sin(30) = ${sin(30)}');
  print('cos(60) = ${cos(60)}');
  print('tan(90) = ${tan(90)}');
  print('arc sin(90) = ${asin(90)}');
  print('arc cos(60) = ${acos(60)}');
  print('arc tan(30) = ${atan(30)}');
  print('pie = $pi');
  print('conversion to degree = ${180 / pi}');
  print('Exponential = ${exp(5)}');
  print('log = ${log(5)}');
  print('square root = ${sqrt(5)}');
  print('square root 0.5 = $sqrt1_2');
  print('e = $e');
  print('2^3 = ${pow(2, 3)}');
  print('max(2 and 3) = ${max(2, 3)}');
  print('min(2 and 3) = ${min(2, 3)}');
  print('natural log = $e');
  print('natural log 2 = $ln2');
  print('natural log 10 = $ln10');
  print('log 10e = $log10e');
  print('log 10e = $log10e');
}
